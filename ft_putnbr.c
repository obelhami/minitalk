#include "minitalk.h"

void	ft_putnbr(int n)
{
	long	nb;
	int		i;
	char	a;

	nb = n;
	if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	i = 1;
	while (nb / i >= 10)
		i = i * 10;
	while (i > 0)
	{
		a = nb / i + '0';
		write(1, &a, 1);
		nb = nb % i;
		i = i / 10;
	}
}
